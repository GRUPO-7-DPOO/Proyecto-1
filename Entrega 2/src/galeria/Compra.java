package galeria;

public class Compra {
	public String metodoPago;
	public int id;
	public String comprador;
	public String piezaComprada;
	
	
	public Compra(String metodoPago, int id, String comprador, String piezaComprada) {
		super();
		this.metodoPago = metodoPago;
		this.id = id;
		this.comprador = comprador;
		this.piezaComprada = piezaComprada;
	}
	
	
}
